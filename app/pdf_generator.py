def add_page_footer(canvas, doc):
    """
    Footer for every page.
    """

    canvas.saveState()

    canvas.setStrokeColor(PRIMARY)
    canvas.line(
        40,
        40,
        doc.pagesize[0] - 40,
        40,
    )

    canvas.setFont(
        "Helvetica",
        8,
    )

    canvas.drawString(
        40,
        25,
        "Generated by Freight CRM",
    )

    canvas.drawRightString(
        doc.pagesize[0] - 40,
        25,
        f"Page {canvas.getPageNumber()}",
    )

    canvas.restoreState()
