def build_document_section(story, documents):
    """
    Shipment Documents
    """
    if not documents:

        story.append(
        Paragraph(
            "No shipment documents available.",
            VALUE_STYLE,
        )
    )

    story.append(Spacer(1, 18))
    return
    story.append(
        Paragraph(
            "Shipment Documents",
            SECTION_STYLE,
        )
    )

    rows = [[
        "Document",
        "Type",
        "Status",
        "Remarks",
    ]]

    for d in documents:

        rows.append([
            d.document_name,
            d.document_type.replace("_", " ").title(),
            d.status.replace("_", " ").title(),
            d.remarks or "-"
        ])

    table = Table(
        rows,
        colWidths=[
            2.2 * inch,
            1.4 * inch,
            1.2 * inch,
            2.2 * inch,
        ],
    )

    style = TableStyle([
        ("BACKGROUND", (0, 0), (-1, 0), BLUE),
        ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
        ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
        ("GRID", (0, 0), (-1, -1), .30, colors.grey),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 7),
        ("TOPPADDING", (0, 0), (-1, -1), 7),
    ])

    for r in range(1, len(rows)):

        status = rows[r][2].lower()

        if status == "received":

            style.add(
                "BACKGROUND",
                (2, r),
                (2, r),
                GREEN_BG,
            )

        elif status == "pending":

            style.add(
                "BACKGROUND",
                (2, r),
                (2, r),
                ORANGE_BG,
            )

        else:

            style.add(
                "BACKGROUND",
                (2, r),
                (2, r),
                LIGHT_GRAY,
            )

    table.setStyle(style)

    story.append(table)
    story.append(Spacer(1, 18))


