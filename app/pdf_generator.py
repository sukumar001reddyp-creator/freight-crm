def build_workflow_section(story, shipment, milestones):
    """
    Shipment Operational Workflow
    """

    story.append(
        Paragraph(
            "Operational Workflow",
            SECTION_STYLE,
        )
    )

    workflow = [
        "booked",
        "cargo_picked_up",
        "in_transit",
        "arrived_destination",
        "customs_clearance",
        "out_for_delivery",
        "delivered",
        "closed_completed",
    ]

    labels = {
        "booked": "Booked",
        "cargo_picked_up": "Cargo Picked Up",
        "in_transit": "In Transit",
        "arrived_destination": "Arrived Destination",
        "customs_clearance": "Customs Clearance",
        "out_for_delivery": "Out For Delivery",
        "delivered": "Delivered",
        "closed_completed": "Closed",
    }

    completed = {}

    for m in milestones:
        completed[m.stage] = m

    rows = [["Stage", "Status", "Completed By", "Completed On"]]

    for stage in workflow:

        if stage in completed:

            m = completed[stage]

            rows.append([
                labels[stage],
                "Completed",
                m.completed_by.full_name if m.completed_by else "-",
                m.completed_at.strftime("%d %b %Y %H:%M")
                if m.completed_at else "-"
            ])

        else:

            rows.append([
                labels[stage],
                "Pending",
                "-",
                "-"
            ])

    table = Table(
        rows,
        colWidths=[
            2.4 * inch,
            1.2 * inch,
            1.5 * inch,
            1.5 * inch,
        ],
    )

    style = TableStyle([
        ("BACKGROUND", (0, 0), (-1, 0), PRIMARY),
        ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
        ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
        ("GRID", (0, 0), (-1, -1), .30, colors.grey),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 7),
        ("TOPPADDING", (0, 0), (-1, -1), 7),
    ])

    for r in range(1, len(rows)):

        if rows[r][1] == "Completed":
            style.add(
                "BACKGROUND",
                (1, r),
                (1, r),
                GREEN_BG,
            )
            style.add(
                "TEXTCOLOR",
                (1, r),
                (1, r),
                GREEN,
            )

        else:
            style.add(
                "BACKGROUND",
                (1, r),
                (1, r),
                ORANGE_BG,
            )
            style.add(
                "TEXTCOLOR",
                (1, r),
                (1, r),
                ORANGE,
            )

    table.setStyle(style)

    story.append(table)
    story.append(Spacer(1, 18))


