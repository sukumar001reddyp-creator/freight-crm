def build_shipment_summary(story, shipment):
    """
    Shipment Summary Section
    """

    story.append(
        Paragraph(
            "Shipment Summary",
            SECTION_STYLE,
        )
    )

    status = (
        shipment.shipment_status
        .replace("_", " ")
        .title()
    )

    mode = (
        shipment.mode_of_shipment
        .replace("_", " ")
        .title()
    )

    etd = (
        shipment.etd.strftime("%d %b %Y")
        if shipment.etd
        else "-"
    )

    eta = (
        shipment.eta.strftime("%d %b %Y")
        if shipment.eta
        else "-"
    )

    generated = datetime.now().strftime("%d %b %Y %I:%M %p")

    data = [
        ["Shipment Ref", shipment.shipment_reference],
        ["Client", shipment.client.company_name],
        ["Status", status],
        ["Mode", mode],
        ["Origin", shipment.origin],
        ["Destination", shipment.destination],
        ["ETD", etd],
        ["ETA", eta],
        ["Generated On", generated],
    ]

    table = Table(
        data,
        colWidths=[1.7 * inch, 4.8 * inch],
    )

    table.setStyle(
        TableStyle(
            [
                ("BACKGROUND", (0, 0), (0, -1), PRIMARY_LIGHT),
                ("TEXTCOLOR", (0, 0), (0, -1), PRIMARY),
                ("FONTNAME", (0, 0), (0, -1), "Helvetica-Bold"),
                ("FONTNAME", (1, 0), (1, -1), "Helvetica"),
                ("GRID", (0, 0), (-1, -1), 0.3, colors.grey),
                ("BOTTOMPADDING", (0, 0), (-1, -1), 7),
                ("TOPPADDING", (0, 0), (-1, -1), 7),
                ("BACKGROUND", (1, 0), (1, -1), colors.white),
            ]
        )
    )

    story.append(table)
    story.append(Spacer(1, 18))


