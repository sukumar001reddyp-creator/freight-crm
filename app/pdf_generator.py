# GENERATE SHIPMENT PDF
# ==========================================================

def generate_shipment_pdf(
    shipment,
    milestones,
    documents,
    customs,
    closure,
):
    """
    Generate Professional Shipment PDF
    """

    buffer = BytesIO()

    doc = SimpleDocTemplate(
        buffer,
        rightMargin=30,
        leftMargin=30,
        topMargin=35,
        bottomMargin=45,
    )

    story = []

    # ----------------------------------------
    # Header
    # ----------------------------------------

    draw_header(story)

    # ----------------------------------------
    # Shipment Summary
    # ----------------------------------------

    build_shipment_summary(
        story,
        shipment,
    )

    # ----------------------------------------
    # Client
    # ----------------------------------------

    build_client_information(
        story,
        shipment,
    )

    # ----------------------------------------
    # Shipment Details
    # ----------------------------------------

    build_shipment_details(
        story,
        shipment,
    )

    # ----------------------------------------
    # Workflow
    # ----------------------------------------

    build_workflow_section(
        story,
        shipment,
        milestones,
    )

    # ----------------------------------------
    # Documents
    # ----------------------------------------

    build_document_section(
        story,
        documents,
    )

    # ----------------------------------------
    # Customs
    # ----------------------------------------

    build_customs_section(
        story,
        customs,
    )

    # ----------------------------------------
    # Closure
    # ----------------------------------------

    build_closure_section(
        story,
        closure,
    )

    # ----------------------------------------
    # Footer
    # ----------------------------------------

    doc.build(
        story,
        onFirstPage=add_page_elements,
        onLaterPages=add_page_elements,
    )

    buffer.seek(0)

    return buffer