def draw_watermark(canvas, doc):
    """
    Draw Confidential watermark
    """

    canvas.saveState()

    canvas.setFont(
        "Helvetica-Bold",
        60,
    )

    canvas.setFillColor(
        colors.Color(
            .92,
            .92,
            .92,
            alpha=.25,
        )
    )

    canvas.translate(
        doc.pagesize[0] / 2,
        doc.pagesize[1] / 2,
    )

    canvas.rotate(45)

    canvas.drawCentredString(
        0,
        0,
        "CONFIDENTIAL",
    )

    canvas.restoreState()


